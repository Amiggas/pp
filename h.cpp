#include <iostream>
#include <cmath>

using namespace std;

int main() {
    int a = 5;
    int b = 10;

    
    cout << "Max of " << a << " and " << b << " is: " << max(a, b) << endl;
    cout << "Min of " << a << " and " << b << " is: " << min(a, b) << endl;

  
    double n = 64;
    cout << "Square root of " << n << " is: " << sqrt(n) << endl;

   
    double v = 2.6;
    cout << "Round of " << v << " is: " << round(v) << endl;

  
    double x = 2;
    cout << "Natural log of " << x << " is: " << log(x) << endl;

    return 0;
}
